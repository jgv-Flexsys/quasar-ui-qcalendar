<template>
  <div class="subcontent">
    <navigation-bar @today="onToday" @prev="onPrev" @next="onNext" />

    <div class="row justify-center">
      <div
        class="q-gutter-md"
        style="display: flex; flex-direction: column; max-width: 800px; width: 90%; height: 500px"
      >
        <q-calendar-day
          ref="calendar"
          v-model="selectedDate"
          view="week"
          :disabled-days="disabledDays"
          no-active-date
          bordered
          animated
          transition-next="slide-left"
          transition-prev="slide-right"
          @change="onChange"
          @moved="onMoved"
          @click-date="onClickDate"
          @click-time="onClickTime"
          @click-interval="onClickInterval"
          @click-head-intervals="onClickHeadIntervals"
          @click-head-day="onClickHeadDay"
        />
        <q-calendar-day
          ref="calendar"
          v-model="selectedDate"
          view="week"
          :disabled-days="disabledDays"
          no-active-date
          bordered
          animated
          transition-next="slide-left"
          transition-prev="slide-right"
          @change="onChange"
          @moved="onMoved"
          @click-date="onClickDate"
          @click-time="onClickTime"
          @click-interval="onClickInterval"
          @click-head-intervals="onClickHeadIntervals"
          @click-head-day="onClickHeadDay"
        />
      </div>
    </div>
  </div>
</template>

<script>
import { QCalendarDay, addToDate, parseTimestamp, today } from '@quasar/quasar-ui-qcalendar/src'
import '@quasar/quasar-ui-qcalendar/src/QCalendarVariables.scss'
import '@quasar/quasar-ui-qcalendar/src/QCalendarTransitions.scss'
import '@quasar/quasar-ui-qcalendar/src/QCalendarDay.scss'

import { defineComponent } from 'vue'
import NavigationBar from 'components/NavigationBar.vue'

export default defineComponent({
  name: 'WeekDisabledDays',
  components: {
    NavigationBar,
    QCalendarDay,
  },
  data() {
    return {
      selectedDate: today(),
    }
  },
  computed: {
    disabledDays() {
      const days = []
      const ts = parseTimestamp(today())
      // make next 4 days, after today, disabled
      Array.from(Array(4)).forEach((_, i) => {
        days.push(addToDate(ts, { day: i + 1 }).date)
      })
      return days
    },
  },
  methods: {
    onToday() {
      this.$refs.calendar.moveToToday()
    },
    onPrev() {
      this.$refs.calendar.prev()
    },
    onNext() {
      this.$refs.calendar.next()
    },
    onMoved(data) {
      console.log('onMoved', data)
    },
    onChange(data) {
      console.log('onChange', data)
    },
    onClickDate(data) {
      console.log('onClickDate', data)
    },
    onClickTime(data) {
      console.log('onClickTime', data)
    },
    onClickInterval(data) {
      console.log('onClickInterval', data)
    },
    onClickHeadIntervals(data) {
      console.log('onClickHeadIntervals', data)
    },
    onClickHeadDay(data) {
      console.log('onClickHeadDay', data)
    },
  },
})
</script>
